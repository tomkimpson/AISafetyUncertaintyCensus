# --------------------------------------------------------------------------- #
# LaTeX escaping for free-text cells (census carries lots of unicode/math).
# --------------------------------------------------------------------------- #
_UNICODE = [
    ("×", r"$\times$"), ("≥", r"$\ge$"), ("≤", r"$\le$"), ("→", r"$\to$"),
    ("±", r"$\pm$"), ("≈", r"$\approx$"), ("–", "--"), ("—", "---"),
    ("’", "'"), ("‘", "'"), ("“", "``"), ("”", "''"),
    ("§", r"\S{}"), ("<", r"$<$"), (">", r"$>$"), ("~", r"$\sim$"),
]


def latex_escape(text: str) -> str:
    text = text.strip()
    # Strip Markdown bold markers carried over from the source cells (e.g. the
    # emphasised "**± 12.9%**" UK AISI margins) -- they are source emphasis, not
    # table content.
    text = text.replace("**", "")
    # Protect specials first (order matters: backslash before others).
    text = text.replace("\\", r"\textbackslash{}")
    for ch in ("&", "%", "#", "_", "{", "}"):
        text = text.replace(ch, "\\" + ch)
    for uni, rep in _UNICODE:
        text = text.replace(uni, rep)
    return text


