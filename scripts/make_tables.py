def _decision_short(v):
    return {
        "BELOW DEMONSTRATED": "below demonstrated",
        "ABOVE DEMONSTRATED": "above demonstrated",
        "INDETERMINATE": r"\textbf{indeterminate}",
    }.get(v, latex_escape(v))


def build_audit_table(census_id_map=None):
    census_id_map = census_id_map or {}
    src = DERIVED / "audited.csv"
    rows = list(csv.DictReader(src.open()))
    body = []
    sensitivity_body = []
    for r in rows:
        cd = r["critical_deff"]
        if r["assumed_m"] == "":
            cd_str = "n/a"
        elif cd:
            cd_str = (
                f"{cd} ({r['critical_icc_m5']}/"
                f"{r['critical_icc_m10']}/{r['critical_icc_m20']})"
            )
        else:
            cd_str = "---"
        # +/-1-count sensitivity marker
        pm1 = r"\checkmark" if r["count_sensitive"] == "1" else "---"
        eval_name = {
            "swebench_hard": "SWE-bench hard",
            "metr_dedup": "METR dedup",
            "bioweapons_knowledge": "Bioweapons knowl.",
        }.get(r["capability"], r["capability"])
        # Cross-reference the row's ID in the full census (Table 4).
        cid = r.get("record_id") or census_id_map.get((r["capability"], r["model"]))
        if cid:
            eval_name = f"{eval_name} ({cid})"
        ci = f"{float(r['directional_low']):.2f}--{float(r['directional_high']):.2f}"
        central_ci = f"{float(r['two_sided_low']):.2f}--{float(r['two_sided_high']):.2f}"
        two_sided = "straddles" if r["unresolved_two_sided"] == "1" else "clears"
        body.append(
            f"        {eval_name} & {latex_escape(r['model'])} & {r['n']} & "
            f"{float(r['score']):.2f} & {ci} & {float(r['threshold']):.2f} & "
            f"{_decision_short(r['decision_class'])} & "
            f"{float(r['prob_above']):.3f} \\\\"
        )
        sensitivity_body.append(
            f"        {eval_name} & {latex_escape(r['model'])} & {central_ci} & "
            f"{two_sided} & {r['required_n']} & {pm1} & {cd_str} \\\\"
        )

    direct = [r for r in rows if r["capability"] != "swebench_hard"]
    swe = [r for r in rows if r["capability"] == "swebench_hard"]

    def class_counts(selected, field="decision_class"):
        return tuple(
            sum(r[field] == label for r in selected)
            for label in (
                "BELOW DEMONSTRATED", "INDETERMINATE", "ABOVE DEMONSTRATED"
            )
        )

    direct_counts = class_counts(direct)
    swe_task_counts = class_counts(swe)
    swe_run_counts = class_counts(swe, "decision_class_runs")
    two_sided_task = sum(int(r["unresolved_two_sided"]) for r in rows)
    two_sided_mixed = (
        sum(int(r["unresolved_two_sided"]) for r in direct)
        + sum(int(r["unresolved_two_sided_runs"]) for r in swe)
    )

    tex = r"""% GENERATED by scripts/make_tables.py -- do not edit by hand.
\begin{table}[t]
    \centering
    \small
    \begin{tabular}{llcccccc}
        \toprule
        eval & model & $n$ & $\hat p$ & directional 95\% bounds & $\tau$ & primary class & $P(p>\tau)$ \\
        \midrule
""" + "\n".join(body) + r"""
        \bottomrule
    \end{tabular}
    \caption{Conditional IID-Bernoulli audit of ten model-level results. The
    primary class is three-way: ``below demonstrated'' when the one-sided 95\%
    upper Wilson bound is below $\tau$, ``above demonstrated'' when the
    one-sided 95\% lower bound is above $\tau$, and ``indeterminate'' otherwise.
    These are two separate claims, each controlled at $\alpha=0.05$; the displayed
    pair equals the endpoints of a central 90\% interval and is \emph{not} a
    simultaneous 95\% classification. Below/indeterminate/above counts are
    <<DIRECT_COUNTS>> for the six direct-count rows, <<SWE_TASK_COUNTS>> for
    SWE-bench at task level, and <<SWE_RUN_COUNTS>> under the run-level
    sensitivity ($n=\text{tasks}\times10$).
    $P(p>\tau)$ is the Beta posterior probability under a uniform prior.
    The code after each eval name (e.g.\ ``(A5)'') is that row's ID in the full
    census, \Cref{tab:census}, where these rows are shaded. Full two-sided,
    sample-size, count, and clustering sensitivities appear in
    \Cref{tab:audit-sensitivity}.}
    \label{tab:audit}
\end{table}
"""
    tex = (
        tex.replace("<<DIRECT_COUNTS>>", "/".join(map(str, direct_counts)))
        .replace("<<SWE_TASK_COUNTS>>", "/".join(map(str, swe_task_counts)))
        .replace("<<SWE_RUN_COUNTS>>", "/".join(map(str, swe_run_counts)))
    )
    (TABLES / "audit.tex").write_text(tex)

    sensitivity_tex = r"""% GENERATED by scripts/make_tables.py -- do not edit by hand.
\begin{table}[t]
    \centering
    \scriptsize
    \resizebox{\textwidth}{!}{%
    \begin{tabular}{llcclcl}
        \toprule
        eval & model & central 95\% Wilson & central relation & $n_{\mathrm{req}}$ & $\pm$1 & crit.\ DEFF (ICC@$m=5/10/20$) \\
        \midrule
""" + "\n".join(sensitivity_body) + r"""
        \bottomrule
    \end{tabular}%
    }
    \caption{Sensitivity details for \Cref{tab:audit}. The central two-sided
    95\% interval straddles $\tau$ for <<TWO_SIDED_TASK>> of ten task-level rows
    (<<TWO_SIDED_MIXED>> of ten if SWE uses run-level denominators).
    $n_{\mathrm{req}}$ is the one-sided sample size for 95\% power at the observed
    gap. \textbf{$\pm$1} flags a primary class that changes if a reconstructed
    count moves by one. Critical DEFF is the smallest design effect at which the
    directional bounds begin to straddle; parenthesised values translate it to
    ICC at $m=5,10,20$. ``n/a'' marks METR repeated-run rows, for which item
    clustering is inapplicable (not evidence of independence).}
    \label{tab:audit-sensitivity}
\end{table}
"""
    sensitivity_tex = (
        sensitivity_tex.replace("<<TWO_SIDED_TASK>>", str(two_sided_task))
        .replace("<<TWO_SIDED_MIXED>>", str(two_sided_mixed))
    )
    (TABLES / "audit_sensitivity.tex").write_text(sensitivity_tex)
    return len(rows)


