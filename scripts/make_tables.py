"""Generate the paper's LaTeX tables from the derived data, so every number in
the manuscript traces to a script rather than a hand transcription.

Emits into ``paper/tables/``:

* ``uplift.tex``        -- Table 1: ratio-of-means intervals for the bio-uplift
                           trial, every reading x method (from uplift_readings.csv).
* ``audit.tex``         -- Table 2: threshold-vs-bound rows, with the
                           three-way primary class (from audited.csv).
* ``audit_sensitivity.tex`` -- Two-sided, sizing, count, and clustering details.
* ``reporting_levels.tex`` -- Record- and source-document reporting summaries
                              by framework.
* ``census.tex``        -- Appendix corpus longtable, parsed from census_records.csv.
* ``census_counts.tex`` -- \\newcommand macros with the EXACT census counts, so the
                           main-text summary cites auditable numbers, not "~60".
* ``census_sensitivity.tex`` -- Primary-vs-screened coding sensitivity.

Run after uplift_analysis.py, run_audit.py, and summarize_corpus.py (which write
the derived CSVs).
"""

